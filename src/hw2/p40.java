public class p40 {
    public static void main(String[] args) {
        // 直接初始化陣列
        int[] intArray = {123, 456, 789};

        // 使用標準的陣列操作
        for (int i = 0; i < intArray.length; i++) {
            System.out.println("intArray[" + i + "]=" + intArray[i]);
        }
    }
}
